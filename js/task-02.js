const ingredients = [
  'Potatoes',
  'Mushrooms',
  'Garlic',
  'Tomatos',
  'Herbs',
  'Condiments',
];

const list = document.querySelector("#ingredients");

ingredients.forEach((el) => {

  const elementList = document.createElement("li")
  elementList.textContent = `${el}`;
  elementList.classList.add("item");
  list.append(elementList);

})





